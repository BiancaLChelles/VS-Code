# Repetindo tarefas com while

tentativas = 3

# aqui o código irá repetir enquanto a variável tentativas for maior que 0
while tentativas > 0:
    print ("Você ainda tem ", tentativas, "tentativas")
    tentativas -= 1
# aqui o código irá parar de repetir quando a variável tentativas for igual a 0
print ("Suas tentativas acabaram")

