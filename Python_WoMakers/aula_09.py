
# Operadores Aritméticos
 
# Operadores Aritméticos são usados para realizar operações matemáticas entre valores numéricos.

valorA = int(input("Digite o valor A: "))
valorB = int(input("Digite o valor B: "))

resultado_soma = valorA + valorB
resultado_subtracao = valorA - valorB
resultado_divisao = valorA / valorB
resultado_multiplicacao = valorA * valorB
resultado_divisao_inteira = valorA // valorB
resultado_resto_divisao = valorA % valorB
resultado_esponenciacao = valorA ** valorB

print("Soma: ", resultado_soma)
print("Subtração: ", resultado_subtracao)
print("Multiplicação: ", resultado_multiplicacao)
print("Divisão: ", resultado_divisao)
print("Divisão inteira: ", resultado_divisao_inteira)
print("Resto da divisão: ", resultado_resto_divisao)
print("Exponenciação: ", resultado_esponenciacao)


