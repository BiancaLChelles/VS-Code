# Exemplo de operadores
# += Incremento
# -= Decremento
# % Resto da divisão 


valorA = int(input("Digite o valor A: "))
valorB = int(input("Digite o valor B: "))


valorA += 5

valorB -= 2

print ("Valor final:", valorA)
print ("Valor final:", valorB)




if valorA % 2 == 0:
    print ("Valor A é par")
else:
    print ("Valor A é ímpar")