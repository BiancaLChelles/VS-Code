
# Aula 03 - Entrada de dados
# Neste exemplo, vamos aprender a usar a função input() para receber dados do usuário.

print ("Olá!")

nome = input("Qual é o seu nome? ")

print(f"Olá, {nome}!")

idade = input("Qual é a sua idade? ")

print(f"Olá, {nome}! Prazer te conhecer! Você tem {idade} anos.")

