# Tipos de dados em Python incluem: inteiros (int), números de ponto flutuante (float), strings (str) e booleanos (bool).

# Exemplo de tipos de dados
# Variável do tipo inteiro
idade = 25

# Variável do tipo float
altura = 1.75

# Variável do tipo string
nome = "Maria"

# Variável do tipo booleano
estudante = True

# Imprimindo os valores e seus tipos
print("Idade:", idade, "Tipo:", type(idade)) 
print("Altura:", altura, "Tipo:", type(altura))
print("Nome:", nome, "Tipo:", type(nome)) 
print("Estudante:", estudante, "Tipo:", type(estudante))        


